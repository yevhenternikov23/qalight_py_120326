# print("\n=== ЗАВДАННЯ 11: Відгадування числа ===")
# Створіть просту гру "відгадай число"
# Загадане число: 42
# Користувач має 5 спроб

secret_number = 42
attempts = 5

print(f"Відгадайте число від 1 до 100! У вас {attempts} спроб.")

while attempts > 0:
    guess = int(input("Введіть число: "))

    if guess == secret_number:
        print("Ви відгадали число!")
        break
    elif guess < secret_number:
        print("Загадане число більше.")
    else:
        print("Загадане число менше.")

    attempts -= 1
    print(f"Залишилось спроб: {attempts}")

if attempts == 0:
    print(f"Кількість спроб вичерпано. Загадане число було: {secret_number}")

